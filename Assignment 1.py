 # Integer
a = 10
print("Integer:", a, type(a))

# Float
b = 3.14
print("Float:", b, type(b))

# String
c = "Hello"
print("String:", c, type(c))

# Boolean
d = True
print("Boolean:", d, type(d))

# List
e = [1, 2, 3]
print("List:", e, type(e))

# Tuple
f = (1, 2, 3)
print("Tuple:", f, type(f))

# Dictionary
g = {"name": "Aarti", "age": 18}
print("Dictionary:", g, type(g))

# Set
h = {1, 2, 3}
print("Set:", h, type(h))
